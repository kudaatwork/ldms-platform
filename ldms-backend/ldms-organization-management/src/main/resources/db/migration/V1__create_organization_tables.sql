    modified_by VARCHAR(150) NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_org_email (email),
    KEY idx_org_classification (organization_classification),
    KEY idx_org_kyc_status (kyc_status),
    KEY idx_org_is_verified (is_verified),
    KEY idx_org_entity_status (entity_status),
    KEY idx_org_classification_kyc (organization_classification, kyc_status),
