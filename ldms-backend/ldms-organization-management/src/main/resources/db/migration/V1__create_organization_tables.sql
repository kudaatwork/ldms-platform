    location_id BIGINT NULL,
    email VARCHAR(255) NOT NULL,
    phone_number VARCHAR(50) NULL,
    organization_type VARCHAR(50) NULL,
    organization_classification VARCHAR(50) NOT NULL,
    industry_id BIGINT NULL,
    contact_person_first_name VARCHAR(100) NULL,
    contact_person_last_name VARCHAR(100) NULL,
    contact_person_email VARCHAR(255) NULL,
    contact_person_phone_number VARCHAR(50) NULL,
    contact_person_position VARCHAR(100) NULL,
    contact_person_gender VARCHAR(50) NULL,
    contact_person_national_id_number VARCHAR(100) NULL,
    contact_person_national_id_upload_id BIGINT NULL,
    contact_person_passport_number VARCHAR(100) NULL,
    contact_person_passport_upload_id BIGINT NULL,
    contact_person_date_of_birth VARCHAR(20) NULL,
    logo_upload_id BIGINT NULL,
    is_verified BOOLEAN NOT NULL DEFAULT FALSE,
    created_via_signup BOOLEAN NULL,
    registration_number VARCHAR(100) NULL,
    tax_number VARCHAR(100) NULL,
    registration_certificate_upload_id BIGINT NULL,
    tax_clearance_certificate_upload_id BIGINT NULL,
    business_license_upload_id BIGINT NULL,
    proof_of_address_upload_id BIGINT NULL,
    representative_national_id_number VARCHAR(100) NULL,
    representative_passport_number VARCHAR(100) NULL,
    industry_specific_license_upload_id BIGINT NULL,
    website_url VARCHAR(500) NULL,
    organization_description TEXT NULL,
