CREATE TABLE industry (
    id BIGINT NOT NULL AUTO_INCREMENT,
    name VARCHAR(200) NOT NULL,
    industry_code VARCHAR(50) NULL,
    description VARCHAR(500) NULL,
    regulatory_body_name VARCHAR(200) NULL,
    regulatory_body_contact_info VARCHAR(300) NULL,
    compliance_requirements VARCHAR(1000) NULL,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    entity_status VARCHAR(50) NOT NULL DEFAULT 'ACTIVE',
    created_at DATETIME(6) NOT NULL,
    created_by VARCHAR(150) NOT NULL,
    modified_at DATETIME(6) NULL,
    modified_by VARCHAR(150) NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_industry_name (name),
    KEY idx_industry_active (active)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

