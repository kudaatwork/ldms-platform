/**
 * The commercial role a trading partner plays in relation to the owning organisation.
 * <ul>
 *   <li>{@link #CUSTOMER} – the partner buys from the owning organisation.</li>
 *   <li>{@link #SUPPLIER} – the partner sells goods or services to the owning organisation.</li>
 * </ul>
 */
public enum TradingPartnerRole {
    CUSTOMER,
    SUPPLIER
}
